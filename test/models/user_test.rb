  # end
  def setup
    @user = User.new(name:"example user",email: "user@example.com",
            password: "foobar",password_confirmation: "foobar")
  end

  test "email should be unique" do
    dup_user = @user.dup
    dup_user.email = @user.email.upcase
    @user.save
    assert_not dup_user.valid?
  end

end
